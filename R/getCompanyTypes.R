#' Helper function to return the company types dictionary
#'
#'@return dictionary
#'
#' @author Layla Rohkohl, \email{byehity@gmail.com}
#'
#' @examples
#' getCompanyTypes()
#'
#' @export
#'
getCompanyTypes <- function() {
  return(c("for_profit" = "For Profit",
           "non_profit" = "Non-profit"))
}

